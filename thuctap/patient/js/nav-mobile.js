$(document).ready(function(){
    $("#toggle-menu").click(function(){
        $("#main-menu").css({
            "display":"block",
            "position":"fixed",
            "height":"100%",
            "background":"white",
            "top":0,
            "left":0,
            "z-index":1
        })
        $("#toggle-menu").css({
            "display":"none"
        })
        $("#black-screen").css({
            "display":"block"
        })
        $(".nav-mobile").css({
            "padding-left":"44%"
        })
    })
    $(".btn-hide").click(function(){
        $("#main-menu").css({
            "display":"none"
        })
        $("#toggle-menu").css({
            "display":"inline"
        })
        $("#black-screen").css({
            "display":"none"
        })
        $(".nav-mobile").css({
            "padding-left":0
        })
    })
});